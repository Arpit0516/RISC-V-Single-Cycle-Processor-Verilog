`timescale 1ns / 1ps
//////////////////////////////////////////////////////////////////////////////////
// 32-bit 2:1 Multiplexer
//////////////////////////////////////////////////////////////////////////////////

module mux2_1_32bit(
    input  [31:0] in0,
    input  [31:0] in1,
    input         sel,
    output [31:0] out
);

assign out = (sel) ? in1 : in0;

endmodule